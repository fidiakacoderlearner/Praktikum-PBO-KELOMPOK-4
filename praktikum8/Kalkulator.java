    private void handleNumber(String number){
        character += number;
        outputField.setText(character);
    }
  
    private void handleOperator(String operator, int optionOperator){
        if (character.isEmpty() || character.equals("-")){
            return;
        }
        value1 = Double.parseDouble(outputField.getText());
        outputField.setText(operator);
        character = "";
        operatorType = optionOperator;
    }
    
    private void updateOutput(double currentResult){
        if(currentResult % 1 == 0){
            outputField.setText(String.valueOf((int) currentResult));
            character = Integer.toString((int) currentResult);
            return;
        }
        outputField.setText(String.valueOf(currentResult));
        character = Double.toString(currentResult);
    }
    
    private void resetCalculator(){
        character = "";
        result = 0.0;
        value1 = 0.0;
        value2 = 0.0;
        operatorType = 0;
    }
    
