    private void tombolCekDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolCekDataActionPerformed
        String nim = inputNim.getText();
        boolean ditemukan = false;
        try {
            Long.parseLong(nim);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Silakan masukkan NIM berupa angka");
            return;
        }

        for (Mahasiswa m : Mahasiswa.mahasiswas) {
            if (m.getNim().equals(nim)) {
                inputNama.setText(m.getNama());
                inputProdi.setSelectedItem(m.getProdi());
                inputEditNim.setText(m.getNim());
                if (m.getJenisKelamin().equals("Laki - Laki")) {
                    tombolLakiLaki.setSelected(true);
                } else {
                    tombolPerempuann.setSelected(true);
                }
                checkBoxStatus.setSelected(m.isIsActive());
                ditemukan = true;
                enableEditing();
                break;
            }
        }
        if(!ditemukan){
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan");
        }
    }//GEN-LAST:event_tombolCekDataActionPerformed

