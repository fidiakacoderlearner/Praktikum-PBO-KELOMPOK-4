    private void tombolUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolUbahActionPerformed
        // TODO add your handling code here:
        String nim = inputNim.getText(); // ambil NIM yang diubah
        String editNim = inputEditNim.getText();
        String nama = inputNama.getText();
        String prodi = inputProdi.getSelectedItem().toString();
        String jenisKelamin = tombolLakiLaki.isSelected() ? "Laki - Laki" : "Perempuan";
        boolean aktif = checkBoxStatus.isSelected();

        try {
            Long.parseLong(editNim);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Silakan masukkan NIM baru berupa angka");
            return;
        }
        
        boolean ditemukan = false;
        for (Mahasiswa m : Mahasiswa.mahasiswas) {
            if (m.getNim().equals(nim)) {
                for (Mahasiswa i : Mahasiswa.mahasiswas) {
                    if(i.getNim().equals(editNim) && !(nim.equals(editNim))){
                        JOptionPane.showMessageDialog(this, "NIM baru sudah ada sebelumnya, silakan masukkan NIM lain");
                        return;
                    }
                }
                m.setNama(nama);
                m.setNim(editNim);
                m.setProdi(prodi);
                m.setJenisKelamin(jenisKelamin);
                m.setIsActive(aktif);
                JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
                ditemukan = true;
                break;
            }
        }

        if (!ditemukan) {
            JOptionPane.showMessageDialog(this, "Data dengan NIM " + nim + " tidak ditemukan!");
        }
        clearForm();
        disableEditing();
    }//GEN-LAST:event_tombolUbahActionPerformed

    private void tombolHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolHapusActionPerformed
        // TODO add your handling code here:
        String nim = inputNim.getText(); // ambil NIM dari field input
        Mahasiswa target = null;

        for (Mahasiswa m : Mahasiswa.mahasiswas) {
            if (m.getNim().equals(nim)) {
                target = m;
                break;
            }
        }

        if (target != null) {
            int konfirmasi = JOptionPane.showConfirmDialog(this, 
                "Yakin ingin menghapus data dengan NIM " + nim + "?", 
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

            if (konfirmasi == JOptionPane.YES_OPTION) {
                Mahasiswa.mahasiswas.remove(target);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                // Kosongkan form setelah hapus
                inputNama.setText("");
                inputNim.setText("");
                inputProdi.setSelectedIndex(0);
                jenisKelaminButtonGroup.clearSelection();
                checkBoxStatus.setSelected(false);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan!");
        }
        clearForm();
        disableEditing();
    }//GEN-LAST:event_tombolHapusActionPerformed

